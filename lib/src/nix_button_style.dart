/// The visual style of [NixButton].
enum NixButtonStyle {
  /// Filled button with a solid background of the primary color.
  filled,

  /// Tonal button with a secondary/primary container color background.
  tonal,

  /// Outlined button with a border and transparent background.
  outlined,

  /// Elevated button with a surface color background and shadow/elevation.
  elevated,

  /// Text button with transparent background and no border.
  text,
}
